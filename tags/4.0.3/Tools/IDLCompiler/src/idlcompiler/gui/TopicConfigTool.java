    private void parseTopicConfig(Project project)
    {
        
        
        try
        {
            String topicConfigFile = "file:///" + project.getRunDirectory() + "/" + project.getTopicConfigFile().getName();
            //StaticManager.getManager();
            topicConfigFile = topicConfigFile.replace("\\", "/");
            StaticManager manager = StaticManager.reInitializeManager(topicConfigFile);
            basePortTextField.setText("" + manager.getTopics().get(0).getPort());
            
            
            for (Topic t : manager.getTopics())
            {
                String nextTopicString = "topic name =\"" + t.getName() + "\" type = \"" + t.getTypeID() + "\"";
                topicListData.add(nextTopicString);
                
            }
        }
        catch (Exception e)
        {
            //continue with empty file...
        }
        fileNameTextField.setText(project.getTopicConfigFile().getName());
        topicList.updateUI();
        
    }
    
