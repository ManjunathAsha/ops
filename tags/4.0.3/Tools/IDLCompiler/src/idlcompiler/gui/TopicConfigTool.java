    private void generateConfigFileButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_generateConfigFileButtonActionPerformed
    {//GEN-HEADEREND:event_generateConfigFileButtonActionPerformed
        String outText = "<?xml version=\"1.0\" encoding=\"utf-8\" ?>\n" +
                         "<config>\n\t" ;
        int basePort = Integer.parseInt(basePortTextField.getText());
        int i = 0;
        for (String  s : topicListData)
        {
            int port = basePort + i;
            outText += "\t<" + s + " />\n";
            i++;
            
        }
        outText += "</config>";
        theProject.getTopicConfigFile().setText(outText);
        theProject.getTopicConfigFile().save();
        try
        {
            theProject.save();
        } 
        catch (TopicConfigFileMissingException ex)
        {
            JOptionPane.showMessageDialog(this, ex.getMessage() + ": " + "Set a Topic Config File before saving project.");
            return;
        }
        
        
        
    }//GEN-LAST:event_generateConfigFileButtonActionPerformed

