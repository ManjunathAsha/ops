    private void addJarDepButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJarDepButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        fc.setFileFilter(new JarFileFilter());
        fc.setAcceptAllFileFilterUsed(false);
        fc.setVisible(true);
        
        int result = fc.showOpenDialog(this);
        
        if(result == JFileChooser.APPROVE_OPTION)
        {
            theProject.getJarDependencies().add(FileHelper.getRelativePath(new File(theProject.getRunDirectory()), new File(fc.getSelectedFile().getPath())));
        }
        jarDepList.updateUI();
        
    }//GEN-LAST:event_addJarDepButtonActionPerformed

    private void removeJarDepButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJarDepButtonActionPerformed
        theProject.getJarDependencies().remove(jarDepList.getSelectedValue());
        jarDepList.updateUI();
    }//GEN-LAST:event_removeJarDepButtonActionPerformed

