    private void takeProjectOptions()
    {
        jarDepList.setListData(this.theProject.getJarDependencies());
        topicConfigPackageTextField.setText(theProject.getTopicConfigPackage());
        topicConfigFileTextField.setText(
                FileHelper.getRelativePath(new File(theProject.getRunDirectory()), 
                theProject.getTopicConfigFile().getFile()));
        genJavaCheckBox.setSelected(theProject.isJavaGenerationEnabled());
        genCSCheckBox.setSelected(theProject.isCsGenerationEnabled());
        genCppCheckBox.setSelected(theProject.isCppGenerationEnabled());
        buildAndJarCheckBox.setSelected(theProject.isBuildAndJarEnabled());
    }

    private void applyOptions() throws IOException
    {
        this.theProject.setTopicConfigPackage(topicConfigPackageTextField.getText());
        this.theProject.setTopicConfig(new TopicConfigFile(new File(theProject.getRunDirectory() + "/" + topicConfigFileTextField.getText())));
        this.theProject.enableCodeHighLighting(enableCodeHighLigthingCheckBox.isSelected());
        this.theProject.enableJavaGeneration(genJavaCheckBox.isSelected());
        this.theProject.enableCppGeneration(genCppCheckBox.isSelected());
        this.theProject.enableCsGeneration(genCSCheckBox.isSelected());
        this.theProject.enableBuildAndJar(buildAndJarCheckBox.isSelected());
        
        parentGUI.updateProjectGUIItems();
    }

